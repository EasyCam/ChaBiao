def t(key: str, lang: str = "en", **kwargs: Any) -> str:
    """Translate a key to the given language, with optional format kwargs."""
    strings = _STRINGS.get(lang, _STRINGS[_DEFAULT])
    text = strings.get(key, _STRINGS[_DEFAULT].get(key, key))
    if kwargs:
        text = text.format(**kwargs)
    return text


def detect_system_lang() -> str:
    """Detect system language and return closest supported lang code."""
    import locale

    try:
        sys_lang = locale.getdefaultlocale()[0] or ""
    except Exception:
        sys_lang = ""
    if not sys_lang:
        try:
            import os

            sys_lang = os.environ.get("LANG", os.environ.get("LANGUAGE", ""))
        except Exception:
            pass
    code = sys_lang.split("_")[0].split("-")[0].lower() if sys_lang else "en"
    if code in SUPPORTED_LANGS:
        return code
    return "en"